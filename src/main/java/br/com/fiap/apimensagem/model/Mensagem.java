package br.com.fiap.apimensagem.model;

public class Mensagem {

    private String id;

    private String text;

    public Mensagem() {

    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }


}
